LEVEL_LABELS = {
    "school": "школьник (7–11 класс)",
    "college": "студент колледжа / СПО",
    "university": "студент вуза",
}

VOLUME_LABELS = {
    "1-2": "1–2 страницы",
    "3-5": "3–5 страниц",
    "1-3": "1–3 страницы",
    "5-7": "5–7 страниц",
    "8-10": "8–10 страниц",
    "auto": "оптимальный объём на своё усмотрение",
}


def report_prompt(topic: str, level: str, volume: str) -> str:
    level_label = LEVEL_LABELS.get(level, level)
    volume_label = VOLUME_LABELS.get(volume, volume)
    return f"""Сформируй учебный доклад на тему: {topic}.
Уровень подготовки: {level_label}.
Объем: {volume_label}.
Стиль: понятный, учебный, без лишней воды.
Структура: заголовок, краткий план, основной текст, вывод, список источников.
Добавь 5–7 реальных источников по теме. Не выдумывай источники, авторов, DOI и ссылки. Если не уверен в источнике — не добавляй его.
Текст должен быть связным и подходить для {level_label}."""


def abstract_prompt(topic: str, level: str, volume: str) -> str:
    level_label = LEVEL_LABELS.get(level, level)
    volume_label = VOLUME_LABELS.get(volume, volume)
    return f"""Сформируй учебный реферат на тему: {topic}.
Уровень подготовки: {level_label}.
Объем: {volume_label}.
Стиль: академичный, но понятный.
Структура: титульный лист (без персональных данных), содержание, введение, 2–3 главы, заключение, список источников.
Во введении укажи актуальность, цель и задачи.
В заключении дай краткие выводы.
Добавь 5–7 реальных источников. Не выдумывай источники, авторов, DOI и ссылки. Если не уверен в источнике — не добавляй его."""


def speech_prompt(material: str) -> str:
    return f"""Подготовь речь для выступления на 1–2 минуты по материалу ниже.
Речь должна быть простой, уверенной и подходить для устного выступления.
Не пересказывай весь текст дословно, выдели главное.
Материал: {material}"""


def qa_prompt(material: str) -> str:
    return f"""Составь 5 вопросов и кратких ответов по материалу ниже.
Вопросы должны быть такими, какие может задать преподаватель после выступления.
Ответы должны быть понятными и короткими (2–4 предложения).
Материал: {material}"""


def sources_by_topic_prompt(topic: str, count: str, fmt: str) -> str:
    return f"""Составь список реальных источников по теме: {topic}.

Количество: {count}
Год: только 2021 и новее
Формат оформления: {fmt}

Главное правило:
Не выдумывай источники. Если не уверен, что источник реально существует, не добавляй его.

Используй только:
- действующие стандарты ISO, PMI, IEEE;
- документы WHO, OECD, World Bank, UN, если они подходят к теме;
- актуальные издания известных книг крупных издательств;
- статьи из известных научных журналов, если точно знаешь автора, название, журнал, год и выходные данные.

Запрещено:
- придумывать авторов, названия, журналы, издательства;
- переводить иностранные названия;
- транслитерировать иностранные источники;
- добавлять DOI, ISBN, EDN или URL без полной уверенности;
- дублировать один источник под разными названиями;
- использовать источники до 2021 года;
- добирать список до {count} за счет сомнительных источников.

Если надежных источников меньше, чем {count}, выведи меньше.

Оформление по ГОСТ Р 7.0.5-2008:
- Фамилия, И. О.
- Город : Издательство
- Между элементами ставь: — 
- Иностранные источники оставляй на языке оригинала.
- Не пиши DOI, ISBN, EDN и URL, если не уверен в точности.

Перед выводом проверь каждый источник:
1. Он точно существует?
2. Он не старше 2021 года?
3. Автор, название, год и издательство/журнал указаны точно?
4. Это не дубль?
5. Нет сомнительных DOI, ISBN, EDN или URL?

Если есть сомнение — удали источник.

Ответ:
Только список источников, без комментариев.
В конце добавь:
«Обратите внимание, что вам может потребоваться проверить наличие этих источников в библиотеке или онлайн-базах данных."""


def sources_format_prompt(sources: str, fmt: str) -> str:
    return f"""Оформи список источников в формате {fmt}.
Обязательные правила:
- Для ГОСТ используй ГОСТ Р 7.0.110-2025 с учетом типа источника (статья, книга, стандарт, методматериал, веб-источник).
- Для APA 7 передавай курсив HTML-тегами <i>...</i>, не используй звездочки *.
- Для Vancouver используй нумерованный формат медицинских ссылок.
Не меняй и не дополняй данные источников — используй только то, что указано.
Сохрани исходную нумерацию.
Если по источнику не хватает данных для полного оформления, 
оформи максимально корректно и добавь пометку "(требуется проверка данных)".
Исходные источники:
{sources}"""


